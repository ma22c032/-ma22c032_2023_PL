# Commented out IPython magic to ensure Python compatibility.
import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

# %matplotlib inline

iris = sns.load_dataset('iris')

# Task 1: Make a bar plot of the mean sepal sizes for each species
iris.groupby('species')['sepal_length'].mean().plot(kind='bar')
plt.xlabel('Species')
plt.ylabel('Mean Sepal Length')
plt.title('Mean Sepal Length for Each Species')
plt.show()

# Task 2: Find the min, max, and mean of petal sizes for each species
summary = iris.groupby('species')['petal_length'].agg(['min', 'max', 'mean'])
print(summary)